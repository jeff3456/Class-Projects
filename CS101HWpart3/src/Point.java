public class Point 
{
	//Two protected data fields called "x" and "y" make them a float
	protected float x;
	protected float y;
	
	//Constructor that takes in two args x and y
	//sets x and y to the instance variables x and y
	public Point(float x ,float y)
	{
		this.x = x;
		this.y = y;
	}
}
